<!-- Conditional Statements:

if-else: Executes one block of code if a condition is true, and another block if it's false. -->

<?php

$grade = 80;

if ($grade >= 75) {
  echo 'You passed the exam';
} else {
  echo 'You fail, better luck next time';
}

?>